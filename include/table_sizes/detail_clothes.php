							<div class="heading">Верхняя одежда</div>
							<p>Для определения своего размера вам понадобится только один инструмент — сантиметровая лента. Все полученные измерения нужно будет сравнить с данными в таблице.Проследите, чтобы лента прилегала плотно к телу.</p>
							<p>Чтобы измерить обхват груди, лента должна проходить по выступающим точкам груди спереди. А сбоку через подмышечные впади. Замыкается лента на лопатках.</p>
							<p>При измерении обхвата талии, лента должна проходить по самой узкой части тела, проходя через самую выступающую точку живота. Держите ленту строго горизонтально.</p>
							<p>Обхват бедер измеряется строго горизонтально. Лента должна проходить по бедрам по самым выступающим точкам ягодиц.</p>
