							<div class="heading">Одежда для новорожденных</div>
							<p>Ползунки, распашонки и комбинезоны — первые элементы гардероба малыша. При выборе одежды ориентируйтесь на качество и состав ткани. А также подбирайте размер по росту ребенка. Его измеряют у доктора. Или эту процедуру можно сделать самостоятельно в домашних условиях. Помните, что дети быстро растут. Поэтому одежда не должна идти размер в размер.</p>
						</td>
					</tr>
					<tr>
						<td>
							<div class="heading">Размеры комбинезонов</div>
							<p>Комбинезоны для прогулок имеют свободный крой. Под них можно одевать ползунки, кофты или хлопковые комбинезоны. Даже в подгузнике ребенку будет комфортно. Так как дети быстро растут, верхняя одежда прослужит 1–2 сезона. Поэтому рекомендуем покупать комбинезон максимум за месяц до начала сезона. При выборе ориентируйтесь на рост малыша. Если он 73 см, то ориентируйтесь на длину 75–80 см.</p>
							
